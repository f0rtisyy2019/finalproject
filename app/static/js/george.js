$(document).ready(function () {
    Object.entries(make_model).forEach(function ([key, values]) {
        $('#maker_opt').append('<option class="make" value="' + key + '">' + key + '</option>');
        for (var i = 0; i < values.length; i++) {
            $('#model_opt').append('<option class="model ' + key + '" value="' + values[i] + '">' + values[i] + '</option>');
        }
    });

    $('#maker_opt').change(function () {
        var value = this.value;
        $('option.model').hide();
        $('option.model.' + value).show();
        $('#model_opt').val($('#model_opt option:first').val());
    });

    var frm = $('#used_car_form')
    frm.submit(function (e) {
        console.log($('#maker_opt').val());
        console.log($('#model_opt').val());
        console.log($('#year_opt').val());
        console.log($('#mileage_value').val());
        $.ajax({
            type: "POST",
            url: '/car_price',
            data: {
                'maker': $('#maker_opt').val(),
                'model': $('#model_opt').val(),
                'year': $('#year_opt').val(),
                'mileage': $('#mileage_value').val(),
            },
            success: function (data) {
                $('.car_price_result').empty().append('$' + data);
            }
        });

        e.preventDefault();
    });

});